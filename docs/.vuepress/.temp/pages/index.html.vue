<template><div><h2 id="what-is-psqlpy" tabindex="-1"><a class="header-anchor" href="#what-is-psqlpy"><span>What is PSQLPy</span></a></h2>
<p><code v-pre>PSQLPy</code> is a new Python driver for <code v-pre>PostgreSQL</code> fully written in Rust. It was inspired by <code v-pre>Psycopg3</code> and <code v-pre>AsyncPG</code>.<br>
